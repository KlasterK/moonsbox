uint32_t crc32_zlib(std::span<const uint8_t> data)
{
    uint32_t crc = 0xFFFFFFFF;
    for(uint8_t byte : data)
    {
        crc = CRC32Table[uint8_t(crc) ^ byte] ^ (crc >> 8);
    }
    return ~crc; // crc ^ 0xFFFFFFFF works too
}
